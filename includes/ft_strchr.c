#include "pipex.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i] && s[i] != (char) c)
		i++;
	if (s[i] == '\0' && c != 0)
		return (0);
	return ((char *) s + i);
}
